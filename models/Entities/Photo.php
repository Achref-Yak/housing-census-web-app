<?php

namespace Entities;

use Doctrine\ORM\Mapping as ORM;

/**
 * Entities\Photo
 */
class Photo
{
    /**
     * @var integer $id
     */
    private $id;

    /**
     * @var string $Title
     */
    private $Title;

    /**
     * @var string $URL
     */
    private $URL;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set Title
     *
     * @param string $title
     * @return Photo
     */
    public function setTitle($title)
    {
        $this->Title = $title;
        return $this;
    }

    /**
     * Get Title
     *
     * @return string 
     */
    public function getTitle()
    {
        return $this->Title;
    }

    /**
     * Set URL
     *
     * @param string $uRL
     * @return Photo
     */
    public function setURL($uRL)
    {
        $this->URL = $uRL;
        return $this;
    }

    /**
     * Get URL
     *
     * @return string 
     */
    public function getURL()
    {
        return $this->URL;
    }
}